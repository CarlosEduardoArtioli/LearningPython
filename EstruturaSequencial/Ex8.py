# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês.
# Calcule e mostre o total do seu salário no referido mês.

valor_hora = float(input('Informe o valor_latas ganho por hora trabalhada: '))
horas = int(input('Informe o total de horas trabalhadas no mês: '))

salario = valor_hora * horas

print('O valor_latas total do seu salário no mês é: ', salario)
